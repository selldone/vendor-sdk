import type {VapiArticleTag} from "../VapiArticleTag";

export default function getTags(
  this: VapiArticleTag,
  vendor_id: string | number,
){
  const url = window.VAPI.GET_MY_VENDOR_SHOP_ARTICLE_TAGS(vendor_id);
  return this.getNow<vapi.article.tags.get.IResponse>(url);
}

